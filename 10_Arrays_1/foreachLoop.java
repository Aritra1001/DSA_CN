// In for each loop the i represents the elements of the arrays and not the index of the array elements.
// we can traverse over the values of the arrayh using for each loop


public class foreachLoop {
    public static void main(String[] args) {
        int[] arr = {1,2,2,3,53,78,43};
        for(int i:arr){ 
            System.out.println(i);
        }
    }
}
